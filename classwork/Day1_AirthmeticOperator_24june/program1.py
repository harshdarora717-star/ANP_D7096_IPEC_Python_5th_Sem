# Arithmetic Operators – Real-Life Problem Statements 
# 1. Monthly Salary Calculation 
# Scenario: 
# A company pays an employee a fixed monthly salary and additional incentives based on performance. 
# Problem Statement: 
# Write a Python program to calculate the total monthly salary of an employee by adding the fixed salary 
# and incentive amount. 
# Input: 
# • Basic Salary  
# • Incentive  
# Output: 
# • Total Salary 

salary = float(input("Enter the basic salary: "))
incentive = float(input("Enter the incentive amount: "))
print("The basic salary is:", salary)
print("The incentive amount is:", incentive)
print("The total monthly salary is:", salary + incentive)

# o/p
# Enter the basic salary: 57
# Enter the incentive amount: 49
# The basic salary is: 57.0
# The incentive amount is: 49.0
# The total monthly salary is: 106.0




