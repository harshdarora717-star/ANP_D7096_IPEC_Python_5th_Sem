# 2. Grocery Store Bill 
# Scenario: 
# A customer purchases multiple packets of rice from a grocery store. 
# Problem Statement: 
# Write a Python program to calculate the total cost of rice packets purchased. 
# Input: 
# • Price per packet  
# • Number of packets  
# Output: 
# • Total Bill Amount  
 
price_per_packet = float(input("Enter the price per packet of rice: "))
number_of_packets = int(input("Enter the number of packets purchased: "))   

print("The price per packet is:", price_per_packet)
print("The number of packets purchased is:", number_of_packets)
print("The total bill amount is:", price_per_packet * number_of_packets)


# Enter the price per packet of rice: 50
# Enter the number of packets purchased: 8
# The price per packet is: 50.0
# The number of packets purchased is: 8
# The total bill amount is: 400.0
