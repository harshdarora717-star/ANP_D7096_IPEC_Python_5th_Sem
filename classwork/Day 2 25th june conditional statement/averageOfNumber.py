# program to calulate the average of 5 numbers

num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))
num3 = float(input("Enter third number: "))
num4 = float(input("Enter fourth number: "))
num5 = float(input("Enter fifth number: "))

#------------------------------------------------------------
#displaying the inputs provided by the user
print("\nThe numbers entered are: ", num1, ",", num2, ",", num3, ",", num4, ",", num5)

#------------------------------------------------------------
# displaying the average of the numbers entered by the user
print("\nThe average of the numbers entered is: ", (num1 + num2 + num3 + num4 + num5) / 5)
